package com.sams.inventorymanagement.enums;

/**
 * Enum representing the status of purchase orders.
 * - PENDING: Order is pending and awaiting approval.
 * - APPROVED: Order has been approved.
 * - CANCELED: Order has been canceled.
 * - DELIVERED: Order has been delivered.
 */
public enum OrderStatus {
    PENDING,
    APPROVED,
    CANCELED,
    DELIVERED,
}
