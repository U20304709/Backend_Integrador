package com.sams.inventorymanagement.enums;

/**
 * Enum representing different types of stores.
 * - RETAIL: Retail store.
 * - WAREHOUSE: Warehouse for storing inventory.
 * - WHOLESALE: Wholesale store for bulk sales.
 * - CLUB: Membership-based wholesale club.
 * - SUPERMARKET: Supermarket.
 */
public enum StoreType {
    RETAIL,
    WAREHOUSE,
    WHOLESALE,
    CLUB,
    SUPERMARKET
}
