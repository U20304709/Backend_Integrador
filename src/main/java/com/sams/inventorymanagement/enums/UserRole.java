package com.sams.inventorymanagement.enums;

/**
 * Enum representing the roles of users in the system.
 * - ADMIN: Administrator with full access.
 * - STORE_MANAGER: Store manager responsible for store-related operations.
 * - STORE_STAFF: Store staff with limited access and responsibilities.
 * - USER: Regular user.
 */
public enum UserRole {
    ADMIN,
    STORE_MANAGER,
    STORE_STAFF
}
